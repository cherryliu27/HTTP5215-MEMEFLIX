//event listener for A-Z buttons
var letterBtns = document.querySelectorAll(".alphabet");
var song = document.querySelector(".song");
var initialScreen = document.querySelector(".windowxp");
letterBtns.forEach(function (btn) {
  btn.addEventListener("click", function () {
    //reset color buttons
    resetColor();
    // Highlight the clicked button
    btn.classList.add("selectedKey");
    letter = btn.dataset.id;
    changeSong(letter);
  });
});

//function to reset color text in buttons
function resetColor() {
  letterBtns.forEach(function (btn) {
    btn.classList.remove("selectedKey");
  });
}

//function to play corresponding video
function changeSong(letter) {
  position = getLink(letter);
  iframe.src = videoLinks[letter][position].link;
  song.innerText = videoLinks[letter][position].song;
  initialScreen.style.display = "none";
  muteBtn.src = "./img/icon-unmute.png";
  pauseBtn.src = "./img/icon-pause.png";
}
//function to get the src link from videoLinks array
function getLink(letter) {
  if (videoLinks[letter].length > 1) {
    var position = randNum(videoLinks[letter].length);
    return position;
  } else return 0;
}

//function to generate random number based on number of video links in each alphabet
function randNum(number) {
  return Math.floor(Math.random() * number);
}

//button option to mute
var muteBtn = document.getElementById("mute");
muteBtn.addEventListener("click", function () {
  if (player.isMuted()) {
    player.unMute();
    muteBtn.src = "./img/icon-unmute.png";
  } else {
    player.mute();
    muteBtn.src = "./img/icon-mute.png";
  }
});

//button to play pause
var pauseBtn = document.getElementById("pause");
pauseBtn.addEventListener("click", function () {
  if (player.getPlayerState() == 1) {
    //Returns true if video is playing
    player.pauseVideo();
    pauseBtn.src = "./img/icon-play.png";
  } else if (player.getPlayerState() == 2) {
    player.playVideo();
    pauseBtn.src = "./img/icon-pause.png";
  }
});

// button to shuffle
var shuffleBtn = document.getElementById("shuffle");
shuffleBtn.addEventListener("click", function () {
  var letter = String.fromCharCode(65 + randNum(24));
  resetColor();
  changeSong(letter);
  document.querySelector(`[data-id="${letter}"]`).classList.add("selectedKey");
});
